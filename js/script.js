/* 
#photo-gallery
=========================================== */
$('#photo-gallery').hover(
  function() {
    // マウスホバー時
    $('#bg-white').addClass('photo-hover');
    $('#bg-red').addClass('photo-hover');
    $('#bg-blue').addClass('photo-hover');
  },
  function() {
    // マウスアウト時
    $('#bg-white').removeClass('photo-hover');
    $('#bg-red').removeClass('photo-hover');
    $('#bg-blue').removeClass('photo-hover');
  }
);

